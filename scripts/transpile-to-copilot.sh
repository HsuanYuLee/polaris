#!/usr/bin/env bash
set -euo pipefail

# transpile-to-copilot.sh
#
# Source of truth: .claude/rules/*.md (L1 universal only)
# Generated target: .github/copilot-instructions.md (+ manifest)
#
# Usage:
#   bash scripts/transpile-to-copilot.sh            # apply
#   bash scripts/transpile-to-copilot.sh --check    # verify no drift

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
ROOT_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

CLAUDE_RULES_DIR="$ROOT_DIR/.claude/rules"
GITHUB_DIR="$ROOT_DIR/.github"
GENERATED_DIR="$GITHUB_DIR/.generated"
TARGET_INSTRUCTIONS="$GITHUB_DIR/copilot-instructions.md"
TARGET_MANIFEST="$GENERATED_DIR/copilot-rules-manifest.txt"

CHECK_ONLY=false

while [[ $# -gt 0 ]]; do
  case "$1" in
    --check) CHECK_ONLY=true; shift ;;
    -h|--help)
      sed -n '1,10p' "$0"
      exit 0
      ;;
    *)
      echo "Unknown option: $1" >&2
      exit 1
      ;;
  esac
done

[[ -d "$CLAUDE_RULES_DIR" ]] || {
  echo "Missing source rules directory: $CLAUDE_RULES_DIR" >&2
  exit 1
}

tmp_instructions="$(mktemp)"
tmp_manifest="$(mktemp)"
trap 'rm -f "$tmp_instructions" "$tmp_manifest"' EXIT

# ---------------------------------------------------------------------------
# Build SHA256 manifest (L1 rules only — skip company subdirectories)
# ---------------------------------------------------------------------------
while IFS= read -r f; do
  rel="${f#"$ROOT_DIR"/}"
  sha="$(shasum -a 256 "$f" | awk '{print $1}')"
  printf '%s  %s\n' "$sha" "$rel" >> "$tmp_manifest"
done < <(find "$CLAUDE_RULES_DIR" -maxdepth 1 -type f -name '*.md' | sort)

# ---------------------------------------------------------------------------
# Preamble — static Copilot-specific context
# ---------------------------------------------------------------------------
cat > "$tmp_instructions" << 'PREAMBLE'
# Copilot Instructions — Polaris Workspace

> Auto-generated by `scripts/transpile-to-copilot.sh`.
> Source of truth: `.claude/rules/*.md` — do not edit the Rules section manually.
>
> Regenerate: `bash scripts/transpile-to-copilot.sh`
> Check drift: `bash scripts/transpile-to-copilot.sh --check`

## What This Repo Is

**Polaris** is a [Claude Code](https://docs.anthropic.com/en/docs/claude-code) workspace template that acts as an AI strategist. It routes tasks to specialized **skills**, learns from corrections, and evolves its own rules. The repo is used as a live working instance — not just a static template.

Three pillars:
1. **Development Assistance** — JIRA ticket → branch → code → PR, fully automated
2. **Self-Learning** — corrections are promoted to permanent rules; insights persist cross-session
3. **Daily Operations** — standup, sprint planning, worklog

