# ---------------------------------------------------------------------------
# Append L1 rules (universal only — skip company subdirectories)
# ---------------------------------------------------------------------------
{
  echo
  echo "---"
  echo
  echo "## Rules (Transpiled from .claude/rules/)"
  echo
} >> "$tmp_instructions"

while IFS= read -r f; do
  rel="${f#"$ROOT_DIR"/}"
  printf '### `%s`\n\n' "$rel" >> "$tmp_instructions"
  cat "$f" >> "$tmp_instructions"
  printf '\n\n' >> "$tmp_instructions"
done < <(find "$CLAUDE_RULES_DIR" -maxdepth 1 -type f -name '*.md' | sort)
perl -0pi -e 's/\n+\z/\n/' "$tmp_instructions"

# ---------------------------------------------------------------------------
# Check mode: compare against existing generated files
# ---------------------------------------------------------------------------
if [[ "$CHECK_ONLY" == true ]]; then
  if [[ ! -f "$TARGET_INSTRUCTIONS" || ! -f "$TARGET_MANIFEST" ]]; then
    echo "DRIFT: generated Copilot files are missing. Run transpile first." >&2
    exit 1
  fi
  if ! diff -q "$tmp_instructions" "$TARGET_INSTRUCTIONS" >/dev/null 2>&1; then
    echo "DRIFT: $TARGET_INSTRUCTIONS is out of date." >&2
    diff -u "$TARGET_INSTRUCTIONS" "$tmp_instructions" | head -30 >&2
    exit 1
  fi
  if ! diff -q "$tmp_manifest" "$TARGET_MANIFEST" >/dev/null 2>&1; then
    echo "DRIFT: $TARGET_MANIFEST is out of date." >&2
    exit 1
  fi
  echo "OK: Copilot generated instruction files are in sync."
  exit 0
fi

# ---------------------------------------------------------------------------
# Write mode: generate files
# ---------------------------------------------------------------------------
mkdir -p "$GITHUB_DIR" "$GENERATED_DIR"
cp "$tmp_instructions" "$TARGET_INSTRUCTIONS"
cp "$tmp_manifest" "$TARGET_MANIFEST"
echo "Generated:"
echo "  - $TARGET_INSTRUCTIONS"
echo "  - $TARGET_MANIFEST"
