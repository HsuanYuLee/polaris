## Copilot-Specific: Pre-Check Protocol

Claude Code uses PreToolUse hooks to intercept and block dangerous commands *before* execution. Copilot CLI has no hook system, so the equivalent is a **pre-check pattern**: run the validation script yourself, check its exit code, then decide whether to proceed.

### Deterministic Gates (Pre-Commit)

Before running `git commit`, execute these checks:

| Gate | Script | What it validates |
|------|--------|-------------------|
| Version docs lint | `scripts/check-version-bump-reminder.sh` | If VERSION staged, run readme-lint |
| Task.md schema | `scripts/validate-task-md.sh {path}` | Required sections present |
| Refinement schema | `scripts/validate-refinement-json.sh {path}` | Required fields in refinement.json |
| Refinement inbox schema | `scripts/validate-refinement-inbox-record.sh {path}` | breakdown-produced return inbox has no raw sidecar evidence |
| Task.md deps | `scripts/validate-task-md-deps.sh {dir}` | Dependencies form valid DAG |

### Deterministic Gates (Pre-Push)

Before running `git push`, execute these checks:

| Gate | Script | What it validates |
|------|--------|-------------------|
| CI local | `.claude/scripts/ci-local.sh` (repo-specific, gitignored) | Local CI mirror passes |
| Quality gate | Check `/tmp/.quality-gate-passed-{branch}` | Quality check flow completed |
| Base freshness | `scripts/check-base-fresh.sh --task-md {path}` | Target base branch is recent |

### Deterministic Gates (Pre-PR)

Before running `gh pr create`, execute:

| Gate | Script | What it validates |
|------|--------|-------------------|
| PR base branch | `scripts/resolve-task-base.sh` + compare | `--base` matches task.md expected base |

### Bash Command Rules (Always)

Before running any bash command, self-check:

| Gate | Script | What it validates |
|------|--------|-------------------|
| No `cd` | `scripts/check-no-cd-in-bash.sh --command "..."` | No `cd path && cmd` pattern |
| No independent chaining | `scripts/check-no-independent-cmd-chaining.sh --command "..."` | No `cmd1; cmd2` where cmd2 is independent |

**Exit code convention**: `0` = pass, `1` = retryable error, `2` = hard block (do not proceed).

### Git Hooks

If `scripts/install-copilot-hooks.sh` has been run, the pre-commit and pre-push gates above are enforced automatically by git. The bash command gates (no-cd, no-chaining) remain LLM self-discipline.

## Copilot-Specific: Session Wrap-Up

Claude Code has Stop hooks that fire at session end. In Copilot, achieve the same by explicitly wrapping up before ending:

1. **Check todos** — review all pending tasks; carry forward or close
2. **Session summary** — if substantial work was done, note what was accomplished
3. **Feedback reflection** — if you self-corrected during the session, consider whether it should become a permanent rule

Trigger: user says "收工", "wrap up", "EOD", or "session complete".

## Copilot-Specific: Known Limitations vs Claude Code

| Feature | Claude Code | Copilot CLI | Mitigation |
|---------|------------|------------|------------|
| PreToolUse hooks | Physical blocks (exit 2) | Git hooks + LLM pre-check | Covered above |
| Context compression | PreCompact/PostCompact hooks | Not applicable | Copilot uses larger context windows |
| Stop hooks | Auto-fire at turn end | User-triggered wrap-up | See Session Wrap-Up above |
| Memory hot-tier injection | Auto-loaded every session | Read `memory/` manually on context switch | Ask: "load memory for {topic}" |
| Skill tool | Platform-native routing | Available via Copilot skill tool | Same functionality |

## Conventions

### Skill-First Routing
When a user message matches a skill's trigger pattern, invoke the **Skill tool immediately** — before reading any files, calling any APIs, or running any commands. Skills define their own data-fetching and delegation.

Full routing table: `.claude/rules/skill-routing.md`

### Branch Switching → Always Use Worktrees
Any operation that changes `HEAD`, switches branches, or touches the working tree must run in a `git worktree add` copy instead.

Worktree path convention: `{base_dir}/.worktrees/{repo}-{purpose}-{ticket_or_topic}`

### Bash Commands — No `cd`
Never use `cd <path> && <cmd>`. Use each tool's built-in path parameter.
Independent commands should be issued as parallel tool calls, not chained with `&&`.

### Rules Hierarchy
- **L1** (`.claude/rules/*.md`) — universal, always loaded
- **L2** (`.claude/rules/{company}/*.md`) — company-specific, scope-gated

### Secrets
`.env` is tracked — declare variable names only, leave values empty. Real values go in `.env.local` (gitignored).

### Version Bump → Auto-Sync
After any commit that changes `VERSION`, run `scripts/sync-to-polaris.sh --push`.

